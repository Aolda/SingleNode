def sub_int(x,y) :
    z = int(x) - int(y)
    return z

def sub_float(x,y) :
    z = float(x) - float(y)
    return z

def mul_int(x,y) :
    z = int(x) * int(y)
    return z

def mul_float(x,y) :
    z = float(x) * float(y)
    return z

def div_int(x,y) :
    z = int(x) / int(y)
    return z

def div_float(x,y) :
    z = float(x) / float(y)
    return z

def sum_int(x,y) :
    z = int(x) + int(y)
    return z

def sum_float(x,y) :
    z = float(x) + float(y)
    return z
